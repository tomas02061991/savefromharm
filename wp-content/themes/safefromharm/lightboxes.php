              <p><br>Amur tigers were once found throughout the Russian Far East, northern China and the Korean peninsula. By the 1940s, hunting had driven the Amur tiger to the brink of extinction—with no more than 40 individuals remaining in the wild. The subspecies was saved when Russia became the first country in the world to grant the tiger full protection.<br><br>

              By the 1980s, the Amur tiger population had increased to around 500. Although poaching increased after the collapse of the Soviet Union, continued conservation and antipoaching efforts by many partners—including WWF—have helped keep the population stable at around 540 individuals.<br><br> 

              Amur tigers have the largest home range of any tiger subspecies because they have to search over large areas to find food due to low prey densities. They represent the largest unfragmented tiger population in the world.</p>
